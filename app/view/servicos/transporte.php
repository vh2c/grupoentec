                    <h2>Remoção e Transporte</h2>
                    <p>
                        Pioneira no desenvolvimento de projetos de rigging, a ENTEC possui ampla experiência em movimentação horizontal e vertical de cargas superpesadas e/ou superdimensionadas e reconhecidamente, contribuiu para estabelecer os padrões de serviço neste segmento no estado do Amazonas.<br>
                        Sua equipe técnica está qualificada e capacitada para executar projetos com segurança, precisão e eficiência cumprindo rigorosamente o cronograma contratado. Sua frota composta por guindastes, empilhadeiras, reach stackers, guindautos, pranchas e plataformas é mantida pelo time de manutenção da própria Entec, área que tem status de diretoria, pela importância que é dado às condições de trabalho de seus equipamentos. Além disso, seu ferramental e todos materiais auxiliares de içamento são certificados e controlados (cabos, cintas, manilhas e outros)
                    </p>
