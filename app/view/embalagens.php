                    <h2>Embalagens Especiais</h2>

                    <p>

                        A Entec desenvolveu um sistema padronizado de embalagem industrial composto de chassi fabricado em perfil metálico desenhado e executado sob medida, proteção da carga com aluminização ou shrink-wrapping e fechamento em aço e/ou madeira.  A solução demonstrou ser ideal para acondicionar máquinas, equipamentos eletrônicos ou materiais de qualquer dimensão e peso, de forma estável, segura e controlada.<br>

                        Desenvolvidas especialmente para relocação de plantas e transferência de unidades fabris para outros países, suportam o transporte rodoviário, aéreo ou transoceânico com eficiência e segurança. As embalagens também são apropriadas para situações de armazenagem de produtos de alto valor agregado por longos períodos.<br>

                        O departamento de engenharia da Entec está disponível para desenvolver projetos especiais e de grande complexidade com capacidade de produção e montagem, que permitem atender prazos exigentes. <br>

                    </p>

