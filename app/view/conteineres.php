                    <h2>Locação de Contêineres</h2>

                    <p>

                        A Entec é pioneira e líder de mercado no Amazonas no aluguel de containers marítmos e módulos habitacionais, e no desenvolvimento de projetos especiais customizados de acordo com a necessidade do projeto.<br>

                        A locação de conteineres tem se tornado comum devido ser uma  solução versátil, funcional e economicamente viável, seja em projetos temporários ou permanentes. Possuem diversas vantagens, principalmente quando comparados com construções tradicionais, tais como velocidade para construção, flexibilidade de projeto, portabilidade e a possibilidade de uso em áreas remotas ou de difícil acesso.  <br>

                        Dispomos de mais de 1000 unidades modulares para locação, 20 e 40 pés, para os mais diversos fins como escritório, dormitório, suíte, banheiro, vestiário, cozinha, lavanderia industrial, depósito, camarim, bilheteria, sala de aula, ambulatório, portaria, estandes de vendas ou qualquer outra funcionalidade.<br>

                        Com estrutura verticalizada equipada com caldeiraria, ferramentaria, jateamento, pintura, além de um time de profissionais treinados e capacitados para fabricar e montar os conteineres projetados, a Entec consegue desenvolver os mais diversos arranjos assegurando o mais alto padrão de qualidade, funcionalidade e segurança. <br>

                        Para todas as soluções propostas o serviço de transporte para entrega e retirada dos conteineres é realizado pela Entec com emprego de caminhão guindauto ou guindaste.

                    </p>

