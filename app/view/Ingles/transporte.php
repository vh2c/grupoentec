                    <h2>RIGGING AND TRANSPORTATION</h2>

                    <p>A pioneer in the development of rigging projects, ENTEC has extensive experience in the horizontal and vertical movement of heavy and/or oversized loads and has been widely recognized for contributing to establishing service standards in this segment in the state of Amazonas.</p>

                    <P>Its technical team is qualified and trained to execute projects with safety, precision, and efficiency, strictly adhering to the contracted schedule. The fleet, which includes cranes, forklifts, reach stackers, truck cranes, tractors, trailers, and platforms, is maintained by Entec's own maintenance team—a department with a directorial status, highlighting the importance given to the working conditions of their equipment. Furthermore, all lifting auxiliary tools and materials, such as cables, straps, shackles, and others, are certified and controlled.</P>

